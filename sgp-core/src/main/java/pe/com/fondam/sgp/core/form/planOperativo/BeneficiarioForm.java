package pe.com.fondam.sgp.core.form.planOperativo;

public class BeneficiarioForm {

	private Integer beneficiariosPorResultadoID;
	private Integer tipoBeneficiarioId;
	private String tipoBeneficiarioNombre;
	private String caracteristicasPoblacion;
	private Integer cantidadProgramado;
	private Integer estratoId;
	private String estratoNombre;
	private String descripcion;

	public Integer getBeneficiariosPorResultadoID() {
		return beneficiariosPorResultadoID;
	}

	public void setBeneficiariosPorResultadoID(
			Integer beneficiariosPorResultadoID) {
		this.beneficiariosPorResultadoID = beneficiariosPorResultadoID;
	}

	public Integer getTipoBeneficiarioId() {
		return tipoBeneficiarioId;
	}

	public void setTipoBeneficiarioId(Integer tipoBeneficiarioId) {
		this.tipoBeneficiarioId = tipoBeneficiarioId;
	}

	public String getTipoBeneficiarioNombre() {
		return tipoBeneficiarioNombre;
	}

	public void setTipoBeneficiarioNombre(String tipoBeneficiarioNombre) {
		this.tipoBeneficiarioNombre = tipoBeneficiarioNombre;
	}

	public String getCaracteristicasPoblacion() {
		return caracteristicasPoblacion;
	}

	public void setCaracteristicasPoblacion(String caracteristicasPoblacion) {
		this.caracteristicasPoblacion = caracteristicasPoblacion;
	}

	public Integer getCantidadProgramado() {
		return cantidadProgramado;
	}

	public void setCantidadProgramado(Integer cantidadProgramado) {
		this.cantidadProgramado = cantidadProgramado;
	}

	public Integer getEstratoId() {
		return estratoId;
	}

	public void setEstratoId(Integer estratoId) {
		this.estratoId = estratoId;
	}

	public String getEstratoNombre() {
		return estratoNombre;
	}

	public void setEstratoNombre(String estratoNombre) {
		this.estratoNombre = estratoNombre;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

}

package pe.com.fondam.sgp.core.report;

import java.math.BigDecimal;

public class RptContraPartidaDet {

	private String descripcionAportante;
	private BigDecimal valorUnitario;
	private BigDecimal cantidad;
	private BigDecimal total;

	public String getDescripcionAportante() {
		return descripcionAportante;
	}

	public void setDescripcionAportante(String descripcionAportante) {
		this.descripcionAportante = descripcionAportante;
	}

	public BigDecimal getValorUnitario() {
		return valorUnitario;
	}

	public void setValorUnitario(BigDecimal valorUnitario) {
		this.valorUnitario = valorUnitario;
	}

	public BigDecimal getCantidad() {
		return cantidad;
	}

	public void setCantidad(BigDecimal cantidad) {
		this.cantidad = cantidad;
	}

	public BigDecimal getTotal() {
		return total;
	}

	public void setTotal(BigDecimal total) {
		this.total = total;
	}

}

package pe.com.fondam.sgp.core.view.planOperativo;


public class PlanOperativoView {
	private Integer datoProyectoID;
	private String codigoProyecto;
	private String nombreProyecto;
	private String cantidadPerido;
	private Integer datoPlanOperativoID;
	private String version;
	private Integer estadoPlanOperativo;
	private String estadoPlanOperativoDescripcion;
	
	public Integer getDatoProyectoID() {
		return datoProyectoID;
	}

	public void setDatoProyectoID(Integer datoProyectoID) {
		this.datoProyectoID = datoProyectoID;
	}

	public String getCodigoProyecto() {
		return codigoProyecto;
	}

	public void setCodigoProyecto(String codigoProyecto) {
		this.codigoProyecto = codigoProyecto;
	}

	public String getNombreProyecto() {
		return nombreProyecto;
	}

	public void setNombreProyecto(String nombreProyecto) {
		this.nombreProyecto = nombreProyecto;
	}

	public String getCantidadPerido() {
		return cantidadPerido;
	}

	public void setCantidadPerido(String cantidadPerido) {
		this.cantidadPerido = cantidadPerido;
	}

	public Integer getDatoPlanOperativoID() {
		return datoPlanOperativoID;
	}

	public void setDatoPlanOperativoID(Integer datoPlanOperativoID) {
		this.datoPlanOperativoID = datoPlanOperativoID;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public Integer getEstadoPlanOperativo() {
		return estadoPlanOperativo;
	}

	public void setEstadoPlanOperativo(Integer estadoPlanOperativo) {
		this.estadoPlanOperativo = estadoPlanOperativo;
	}

	public String getEstadoPlanOperativoDescripcion() {
		return estadoPlanOperativoDescripcion;
	}

	public void setEstadoPlanOperativoDescripcion(
			String estadoPlanOperativoDescripcion) {
		this.estadoPlanOperativoDescripcion = estadoPlanOperativoDescripcion;
	}

}
